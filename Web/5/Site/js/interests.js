let build_item = (item_data) => {
  console.log(item_data[1]);
  let block = $("<div/>")
      .addClass('desc_item')
      .append(
          $("<h1/>")
              .attr('id', item_data[0])
              .text(item_data[1]))
      .append($('<br/>'))

  item_data[2].forEach((paragraph) => {
    block.append(
        $('<p/>')
            .text(paragraph)
    );
  });

  return block;
};

let build_hyperlink_list = (interests) => {
  let ul = $('<ul/>');
  interests.forEach((interest) => {
    ul.append(
        $('<li/>').append(
                $('<a/>')
                    .attr('href', '#'.concat(interest[0]))
                    .text(interest[1])
            )
    );
  });
  return $('<figure/>')
      .append($('<figcaption/>').text('Быстрая навигация по странице'))
      .append(ul);
};

let write_all_interests = (interests) => {
  let content = $('<div/>')
      .attr('id', 'content')
      .append(build_hyperlink_list(interests))
      .append($('<br/>'))
      .append($('<br/>'));

  interests.forEach((interest) => {
    content.append('<br/>')
        .append(build_item(interest));
  });

  $('body').append(content);
};

window.onload = () => {
  write_all_interests(aboutme_data);
};
